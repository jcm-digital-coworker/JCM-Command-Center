function bossTypeFor(lastThree: string): BossType {
  return smallBossCastings.includes(lastThree) ? "small" : "large";
}

function castingFromLastThree(lastThree: string): CastingData {
  const bossType = bossTypeFor(lastThree);

  return {
    castingNumber: `70${lastThree}`,
    displayName: castingOds[lastThree] ?? `Casting ${lastThree}`,
    lastThree,
    bossType,
    maxDrillDepth: bossType === "small" ? 1.1 : 1.75,
    maxBoreDepth: bossType === "small" ? 1.1 : 1.75,
    allowedTapCodes:
      bossType === "small"
        ? ["06", "07", "08", "09", "10"]
        : ["06", "07", "08", "09", "10", "11", "12", "13", "14", "15", "16"],
  };
}

